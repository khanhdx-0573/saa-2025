import type { Metadata } from "next";
import { NextIntlClientProvider } from "next-intl";
import { getLocale } from "next-intl/server";
import { montserrat, montserratAlternates } from "@/lib/fonts";
import { createClient } from "@/lib/supabase/server";
import { AuthProvider, type AuthUser } from "@/components/auth/auth-provider";
import { ToastProvider } from "@/components/ui/toast";
import "./globals.css";

export const metadata: Metadata = {
  title: "Sun* Kudos",
  description: "Generated by Sun* Kudos",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const locale = await getLocale();

  const supabase = await createClient();
  // getClaims() throws (not returns an error) when the refresh token is stale/revoked — treat as no session.
  const claims = await supabase.auth.getClaims().then(
    ({ data }) => data?.claims,
    () => undefined
  );
  const initialUser: AuthUser = claims
    ? { id: claims.sub, email: (claims.email as string | undefined) ?? null }
    : null;

  return (
    <html
      lang={locale}
      className={`${montserrat.variable} ${montserratAlternates.variable} h-full antialiased`}
      suppressHydrationWarning
    >
      <body className="min-h-full flex flex-col">
        <NextIntlClientProvider>
          <AuthProvider initialUser={initialUser}>
            <ToastProvider>{children}</ToastProvider>
          </AuthProvider>
        </NextIntlClientProvider>
      </body>
    </html>
  );
}
